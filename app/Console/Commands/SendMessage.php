<?php

namespace App\Console\Commands;

use App\Events\MessagePushed;
use Illuminate\Console\Command;

class SendMessage extends Command
{
    /**
     * The name and signature of the console command.
     *
     * @var string
     */
    protected $signature = 'message:send';

    /**
     * The console command description.
     *
     * @var string
     */
    protected $description = 'It will broadcast a message from the backend to the client side.';

    /**
     * Create a new command instance.
     *
     * @return void
     */
    public function __construct()
    {
        parent::__construct();
    }

    /**
     * Execute the console command.
     *
     * @return mixed
     */
    public function handle()
    {
        // Dispatch an event for pushing a new message. (Broadcast to all recipient)
        event(new MessagePushed(1));
    }
}
